    private void visaProjektInfo(String pid) {
    try {
        String query = "SELECT * FROM projekt WHERE pid = '" + pid + "'";
        List<String> projektInfo = idb.fetchColumn(query);

        if (projektInfo != null && projektInfo.size() > 0) {
            // Fyll i fälten med data
            txtPid.setText(projektInfo.get(0));
            txtProjektNamn.setText(projektInfo.get(1));
            txtStartdatum.setText(projektInfo.get(2));
            txtSlutDatum.setText(projektInfo.get(3));
            txtKostnad.setText(projektInfo.get(4));
            txtPrioritet.setText(projektInfo.get(5));
            txtStatus.setText(projektInfo.get(6));
            txtProjektChef.setText(projektInfo.get(7));
            txtLand.setText(projektInfo.get(8)); // Om Land finns i resultatet
        } else {
            JOptionPane.showMessageDialog(this, "Projektet hittades inte.");
        }
    } catch (InfException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Ett fel inträffade när projektinformationen skulle hämtas.");
    }
}

