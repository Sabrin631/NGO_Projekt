    private void btnHämtaProjektActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHämtaProjektActionPerformed
        // TODO add your handling code here:
        try {
        // Hämta alla projekt där användaren är projektchef
        String query = "SELECT * FROM projekt WHERE projektchef = '" + anvandareID + "'";
        List<String> projektList = idb.fetchColumn(query);

        if (projektList != null && projektList.size() > 0) {
            // Visa projekten i textområdet eller en annan komponent (exempelvis en lista)
            StringBuilder sb = new StringBuilder();
            for (String projektId : projektList) {
                sb.append(projektId).append("\n");
            }
            jTextArea1.setText(sb.toString()); // Fyll i textområdet med projekten
        } else {
            JOptionPane.showMessageDialog(this, "Inga projekt hittades.");
        }
    } catch (InfException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Ett fel inträffade när projekten skulle hämtas.");
    }
    }//GEN-LAST:event_btnHämtaProjektActionPerformed

    private void btnÄndraProjektActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnÄndraProjektActionPerformed
        // TODO add your handling code here:
        
        try {
        // Uppdatera projektet med de nya värdena från textfälten
        String pid = txtPid.getText();
        String projektNamn = txtProjektNamn.getText();
        String startDatum = txtStartdatum.getText();
        String slutDatum = txtSlutDatum.getText();
        String kostnad = txtKostnad.getText();
        String prioritet = txtPrioritet.getText();
        String status = txtStatus.getText();
        String projektChef = txtProjektChef.getText();
        String land = txtLand.getText();

        // Bygg uppdateringsfrågan
        String query = "UPDATE projekt SET projektNamn = '" + projektNamn + "', " +
                       "startDatum = '" + startDatum + "', " +
                       "slutDatum = '" + slutDatum + "', " +
                       "kostnad = '" + kostnad + "', " +
                       "prioritet = '" + prioritet + "', " +
                       "status = '" + status + "', " +
                       "projektchef = '" + projektChef + "', " +
                       "land = '" + land + "' " +
                       "WHERE pid = '" + pid + "'";

        // Använd InfDB:s update-metod
        idb.update(query);

        // Bekräftelse
        JOptionPane.showMessageDialog(this, "Projektinformation har uppdaterats.");
    } catch (InfException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Ett fel inträffade när projektet skulle uppdateras.");
    }
    }//GEN-LAST:event_btnÄndraProjektActionPerformed

