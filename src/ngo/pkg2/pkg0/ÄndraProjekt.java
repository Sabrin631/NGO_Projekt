    private void btnLäggTillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLäggTillActionPerformed
         try{
        String pid = txtpid.getText();
        String projektnamn = txtProjektnamn.getText();
        String beskrivning = txtBeskrivning.getText();
        String Startdatum = txtStartDatum.getText();
        String Slutdatum = txtSlutDatum.getText();
        String Kostnad = txtKostnad.getText();
        String Status = txtStatus.getText();
        String Prioritet = txtPrioritet.getText();
        String ProjektChef = txtProjektChef.getText();
        String Land = txtLand.getText();
        
        String Kontroll = "SELECT COUNT(*) FROM projekt WHERE pid = '" + pid + "'";
        String result = idb.fetchSingle(Kontroll);
        
        if (result != null && Integer.parseInt(result) > 0) {
               JOptionPane.showMessageDialog(this, "pid existerar redan i databasen!", "Fel", JOptionPane.ERROR_MESSAGE);
           return;
                  }
       
        if (pid.isEmpty() || projektnamn.isEmpty() || Status.isEmpty() || Kostnad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fyll i alla obligatoriska fält!", "Fel", JOptionPane.ERROR_MESSAGE);
            return;
               }
         String sql;
                sql = "INSERT INTO projekt (pid, projektnamn, beskrivning, startdatum, slutdatum, kostnad, Status, prioritet, projektchef, land) " +
      "VALUES ('" + pid + "', '" + projektnamn + "', '" + beskrivning + "', '" + Startdatum + "', '" + Slutdatum + "', '" 
      + Kostnad + "', '" + Status + "', '" + Prioritet + "', '" + ProjektChef + "', '" + Land + "')";
         
         
            idb.insert(sql);
             JOptionPane.showMessageDialog(this, "Nytt projket lades till!");
        }catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Fel vid tillägg av projekt: " + ex.getMessage(), "Fel", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLäggTillActionPerformed

    private void btnTaBortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaBortActionPerformed
        // TODO add your handling code here:
        try {
        // Hämta ID från textfältet
        String pid = txtpid.getText();

        // Kontrollera att ID inte är tomt
        if (pid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vänligen ange ett giltigt pid.", "Fel", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Kontrollera om ID:t existerar i databasen
        String checkQuery = "SELECT COUNT(*) FROM projekt WHERE pid = '" + pid + "'";
        String result = idb.fetchSingle(checkQuery);

        if (result == null || Integer.parseInt(result) == 0) {
            JOptionPane.showMessageDialog(this, "Inget Projekt hittades med angivet pid.", "Fel", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Skapa SQL-fråga för att ta bort anställd
        String deleteQuery = "DELETE FROM projekt WHERE pid = '" + pid + "'";
        System.out.println("SQL Query: " + deleteQuery); // Kontrollera frågan

        // Kör borttagningsfrågan
        idb.delete(deleteQuery);

        JOptionPane.showMessageDialog(this, "Projekt borttagen från systemet!");

    } catch (Exception ex) {
        // Visa felmeddelande om något går fel
        JOptionPane.showMessageDialog(this, "Fel vid borttagning av Projekt: " + ex.getMessage(), "Fel", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnTaBortActionPerformed

